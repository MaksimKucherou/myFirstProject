public class Person {

    private static int age;
    private static boolean isMale;
    private static float money;
    private static String name;
    private static short number;
    private static byte house;
    private static char floor;
    private static double random;
    private static long distance;


    public static int getAge() {
        return age;
    }

    public static void setAge(int age) {
        Person.age = age;
    }

    public static boolean getIsMale() {
        return isMale;
    }

    public static void setIsMale(boolean isMale) {
        Person.isMale = isMale;
    }

    public static float getMoney() {
        return money;
    }

    public static void setMoney(float money) {
        Person.money = money;
    }

    public static String getName() {
        return name;
    }

    public static void setName(String name) {
        Person.name = name;
    }

    public static short getNumber() {
        return number;
    }

    public static void setNumber(short number) {
        Person.number = number;
    }

    public static byte getHouse() {
        return house;
    }

    public static void setHouse(byte house) {
        Person.house = house;
    }


    public static char getFloor() {
        return floor;
    }

    public static void setFloor(char floor) {
        Person.floor = floor;
    }

    public static double getRandom() {
        return random;
    }

    public static void setRandom(double random) {
        Person.random = random;
    }


    public static long getDistance() {
        return distance;
    }

    public static void setDistance(long distance) {
        Person.distance = distance;
    }
}






